    /**
     *
     * @param text
     */
    void filter(String text){
        List<ProjectsVo> temp = new ArrayList();
        for(ProjectsVo d: projectListCache){
            if(d.getTitle().toLowerCase().contains(text.toLowerCase())){
                temp.add(d);
            }
        }
        projectList.clear();
        projectList.addAll(temp);
        adapter.notifyDataSetChanged();
    }

