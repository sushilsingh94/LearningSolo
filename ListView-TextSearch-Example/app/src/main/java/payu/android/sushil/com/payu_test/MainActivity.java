    public class GetKickStartProjects extends AsyncTask<Void, Void, String>{

        @Override
        protected String doInBackground(Void... params) {
            InputStream inputStream = null;
            String result = "";
            try {

                HttpClient httpclient = new DefaultHttpClient();
                HttpResponse httpResponse = httpclient.execute(new HttpGet(KIKSTARTER_URL));
                inputStream = httpResponse.getEntity().getContent();
                if(inputStream != null) {
                    result = convertInputStreamToString(inputStream);
                }else {
                    result = "No Projects Found!";
                }
            } catch (Exception e) {
                Log.d("Exception: ", e.getLocalizedMessage());
            }

            return result;
        }

        @Override
        protected void onPostExecute(String s) {
            super.onPostExecute(s);
            try {
                JSONArray responseObj = new JSONArray( s);
                List<ProjectsVo> list = new Gson().fromJson(responseObj.toString(), new TypeToken<List<ProjectsVo>>(){}.getType());

                projectList.addAll(list);
                projectListCache.addAll(list);
                adapter.notifyDataSetChanged();
            } catch (JSONException e) {
                e.printStackTrace();
            }
        }
    }

    /**
     *
     * @param inputStream
     * @return
     * @throws IOException
     */
    private static String convertInputStreamToString(InputStream inputStream) throws IOException {
        BufferedReader bufferedReader = new BufferedReader( new InputStreamReader(inputStream));
        String line = "";
        String result = "";
        while((line = bufferedReader.readLine()) != null)
            result += line;

        inputStream.close();


        return result;

    }
